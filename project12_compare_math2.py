# Dois números serão fornecidos pelo usuário.
# Comparar qual deles é o maior, o menor deve ser apresentado primeiro que o maior.
# Usando a sintaxe "O número x é menor que o Y"

a = int(input("Digite aqui o primeiro número: "))
b = int(input("Digite aqui o primeiro número: "))
if a > b:
    print("O número", b, "é menor que", a)
else:
    print("O número", a, "é menor que", b)

