# Pedir ao usuário para entar com um número menor que 20, se o número for menor que 20 responda obrigado
# Caso contrário exiba, número mais alto que o solicitado.

numero = int(input("Digite aqui um número menor que 20: "))
if numero < 20:
    print("Obrigado!")
else:
    print("Número mais salto que o solicitado!")

