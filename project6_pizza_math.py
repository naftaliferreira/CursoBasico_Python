# Verificar quantas fatias foram comidas de um total e oferecer as restante para o usuário.

a = int(input("Digite aqui a quantidade total de fatias da pizza: "))
b = int(input("Digite aqui a quantidade de fatias comidas: "))
answer = a-b
print("Sobraram", answer, "fatias, pode comer!")

