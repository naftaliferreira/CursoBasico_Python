# Dividir o total da comanda entre todas as pessoas que comeram, para saber o quanto cada um vai pagar.

total_conta = int(input("Qual é o total da comanda? "))
total_pessoas = int(input("Qual é o total de pessoas que comeram? "))
valor_total = total_conta/total_pessoas
print("Cada pessoa irá pagar ", valor_total, "reais")

