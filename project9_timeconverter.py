# Pedir ao usuário um número de dias e converter em horas, minutos e segundos.
dias = int(input("Digite aqui um número de dias: "))
horas = dias * 24
minutos = horas * 60
segundos = minutos * 60
print("Em", dias, "dia tem exatamente: ", horas,
      "horas", minutos, "minutos", segundos, "segundos")

